package fields;

public class OpCode {
    /** Read request */
    public static final short RRQ = 1;

    /** Write request */
    public static final short WRQ = 2;

    /** Data */
    public static final short DATA = 3;

    /** Acknowledgement */
    public static final short ACK = 4;

    /** Error */
    public static final short ERROR = 5;
}
